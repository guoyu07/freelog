}

/**
 * 输出文章标签
 *
 * @access public
 * @param string $split 多个标签之间分隔符
 * @param boolean $link 是否输出链接
 * @param string $default 如果没有则输出
 * @return void
 */
function tags($tags, $split = ',', $link = true, $default = NULL){
    /** 取出tags */
    if ($tags) {
        $result = array();
        foreach ($tags as $tag) {
            $result[] = $link ? '<a href="' . U() . '">'
            . $tag['title'] . '</a>' : $tag['title'];
        }

        echo implode($split, $result);
    } else {
        echo $default;
    }
}
